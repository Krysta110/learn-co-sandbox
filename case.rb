 when "Alice"
  puts "Hello, Alice!"
when "The White Rabbit"
  puts "Dont be late, White Rabbit"
when "Mad Hatter"
  puts "Welcome to the tea party, Mad Hatter"
when "The Queen of Hearts"
  puts "Please dont chop off my head!"
else
  puts "Whoooo are you?"
end
