name = "Alice"

if name == "Alice"
  puts "Hello, Alice!"
elsif name == "The White Rabbit"
  puts "Dont be late, White Rabbit"
elsif name == "Mad Hatter"
  puts "Welcome to the tea party, Mad Hatter"
elsif name == "The Queen of Hearts"
  puts "Please dont chop off my head!"
else
  puts "Whoooo are you?"
end

